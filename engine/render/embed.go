package render

// FrameRenderer описывает минимальный интерфейс для получения
// сгенерированного кадра из 3D-рендера в виде *image.RGBA.
// Его можно будет использовать из Cgo/PyQt для embed-режима.
type FrameRenderer interface {
	// RenderToImage возвращает последний отрендеренный кадр
	// в виде указателя на image.RGBA.
	// Важно: буфер принадлежит движку, его нельзя модифицировать
	// из другого потока одновременно с рендерингом.
	RenderToImage() *image.RGBA
}

